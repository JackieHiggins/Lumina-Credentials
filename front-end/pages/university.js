export default function UniversityPortal() {
  const [studentAddress, setStudentAddress] = useState("");
  const [tokenId, setTokenId] = useState("");
  const [status, setStatus] = useState("");
  const [contractName, setContractName] = useState("");

  // Connects to the provider and fetches the smart contract name.
  async function fetchContractInfo() {
    if (!window.ethereum) return alert("Please install MetaMask!");
    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const contract = new ethers.Contract(CONTRACT_ADDRESS, ABI, provider);
      const fetchedName = await contract.name();
      setContractName(fetchedName);
    } catch (err) {
      console.error(err);
      setStatus("Error: Could not fetch contract info.");
    }
  }

  // Requests a wallet signature to mint an NFT degree to a student.
  async function handleIssueDegree(e) {
    e.preventDefault();
    if (!window.ethereum) return alert("Please install MetaMask!");
    try {
      setStatus("Requesting MetaMask signature...");
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(CONTRACT_ADDRESS, ABI, signer);

      const tx = await contract.issueDegree(studentAddress);
      setStatus("Minting credential on the blockchain...");
      await tx.wait(); 
      setStatus("✓ Success: Degree securely issued to " + studentAddress);
      setStudentAddress(""); 
    } catch (err) {
      setStatus("Error: " + (err.reason || "Transaction failed."));
    }
  }

  // Requests a wallet signature to permanently burn a specific token ID.
  async function handleRevokeDegree(e) {
    e.preventDefault();
    if (!window.ethereum) return alert("Please install MetaMask!");
    try {
      setStatus("Requesting MetaMask signature...");
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(CONTRACT_ADDRESS, ABI, signer);

      const tx = await contract.revokeDegree(parseInt(tokenId));
      setStatus("Burning credential on the blockchain...");
      await tx.wait(); 
      setStatus("✓ Success: Token ID " + tokenId + " has been revoked.");
      setTokenId(""); 
    } catch (err) {
      setStatus("Error: " + (err.reason || "Transaction failed."));
    }
  }

  return (
    <main style={{ padding: '3rem 2rem', maxWidth: '700px', margin: '0 auto' }}>
      <h2 style={{ color: '#0f172a', marginBottom: '2rem', fontSize: '2rem' }}>Administrator Dashboard</h2>
      
      <div className="card" style={{ backgroundColor: '#f8fafc', border: '1px dashed #cbd5e1' }}>
        <h3 style={{ margin: '0 0 10px 0', color: '#334155', fontSize: '1.2rem' }}>Network Connection</h3>
        <button onClick={fetchContractInfo} className="btn-secondary">Connect to Registry</button>
        {contractName && <p style={{ marginTop: '15px', color: '#16a34a', fontWeight: '600' }}>✓ Connected to: {contractName}</p>}
      </div>

      <div className="card">
        <h3 style={{ margin: '0 0 5px 0', color: '#0f172a' }}>Issue Credential</h3>
        <p style={{ color: '#64748b', fontSize: '0.9rem', marginBottom: '15px' }}>Mint a permanent academic record to a student wallet.</p>
        <form onSubmit={handleIssueDegree}>
          <label style={{ fontSize: '0.9rem', fontWeight: '600', color: '#334155' }}>Student Wallet Address</label>
          <input type="text" placeholder="0x..." value={studentAddress} onChange={(e) => setStudentAddress(e.target.value)} className="input-field" required />
          <button type="submit" className="btn-primary">Mint Credential</button>
        </form>
      </div>

      <div className="card">
        <h3 style={{ margin: '0 0 5px 0', color: '#0f172a' }}>Revoke Credential</h3>
        <p style={{ color: '#64748b', fontSize: '0.9rem', marginBottom: '15px' }}>Permanently burn an invalid or revoked record.</p>
        <form onSubmit={handleRevokeDegree}>
          <label style={{ fontSize: '0.9rem', fontWeight: '600', color: '#334155' }}>Token ID</label>
          <input type="number" placeholder="e.g. 0" value={tokenId} onChange={(e) => setTokenId(e.target.value)} className="input-field" required min="0" />
          <button type="submit" className="btn-danger">Burn Credential</button>
        </form>
      </div>

      {status && (
        <div style={{ padding: '16px', backgroundColor: '#e0e7ff', color: '#3730a3', borderRadius: '8px', border: '1px solid #c7d2fe', fontWeight: '500', marginTop: '1rem' }}>
          {status}
        </div>
      )}
    </main>
  );
}